$__EN_TO_MY = array_flip($__MY_TO_EN);

function __(string $text): string
{
	global $__MY_TO_EN, $__EN_TO_MY;
	$lang = $_SESSION['lang'] ?? 'my';
	if ($lang === 'en') {
		if (isset($__MY_TO_EN[$text])) {
			return $__MY_TO_EN[$text];
		}
		// If already English or not mapped, return as-is
		return $text;
	}
	// Burmese target
	if (isset($__EN_TO_MY[$text])) {
		return $__EN_TO_MY[$text];
	}
	return $text;
}
